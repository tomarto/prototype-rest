CREATE TABLE User (
	username VARCHAR(20) PRIMARY KEY,
	first_name VARCHAR(30) NOT NULL,
	last_name VARCHAR(30) NOT NULL,
	password VARCHAR(20) NOT NULL,
	email VARCHAR(50),
	birth_date DATE,
	enabled BOOLEAN DEFAULT TRUE NOT NULL
);

